package basic;

public class User {
  private String username; // Attribute untuk menyimpan username
  private String password; // Attribute untuk menyimpan password

  // Method setter untuk attribute username
  public void setUsername(String username) {
    this.username = username;
  }

  // Method setter untuk attribute password
  public void setPassword(String password) {
    this.password = password;
  }

  // Method getter untuk attribute username
  public String getUsername() {
    return this.username;
  }

  // Method getter untuk attribute password
  public String getPassword() {
    return this.password;
  }
}
